def _load_registry() -> list[dict[str, Any]]:
    if os.path.isfile(_REGISTRY_FILE):
        try:
            with open(_REGISTRY_FILE, "r", encoding="utf-8") as fh:
                data = json.load(fh)
                return data if isinstance(data, list) else []
        except (json.JSONDecodeError, OSError):
            pass
    return []


def _register_project(
    name: str, framework: str, genre: str, path: str
) -> None:
    registry = _load_registry()
    registry.append({
        "name": name,
        "framework": framework,
        "genre": genre,
        "path": path,
        "created_at": time.time(),
    })
    os.makedirs(_DATA_DIR, exist_ok=True)
    with open(_REGISTRY_FILE, "w", encoding="utf-8") as fh:
        json.dump(registry, fh, indent=2, default=str)


