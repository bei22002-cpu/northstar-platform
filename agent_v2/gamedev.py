class Runner(pygame.sprite.Sprite):
    def __init__(self):
        super().__init__()
        self.image = pygame.Surface((30, 50))
        self.image.fill((50, 100, 220))
        self.rect = self.image.get_rect(bottomleft=(80, GROUND_Y))
        self.vel_y = 0
        self.jumping = False
        self.ducking = False

    def jump(self):
        if not self.jumping:
            self.vel_y = -16
            self.jumping = True

    def duck(self, active):
        self.ducking = active
        if active:
            self.image = pygame.Surface((30, 25))
            self.image.fill((50, 100, 220))
            old_bottom = self.rect.bottom
            self.rect = self.image.get_rect(bottomleft=(self.rect.left, old_bottom))
        else:
            self.image = pygame.Surface((30, 50))
            self.image.fill((50, 100, 220))
            old_bottom = self.rect.bottom
            self.rect = self.image.get_rect(bottomleft=(self.rect.left, old_bottom))

    def update(self):
        self.vel_y += 0.9
        self.rect.y += int(self.vel_y)
        if self.rect.bottom >= GROUND_Y:
            self.rect.bottom = GROUND_Y
            self.vel_y = 0
            self.jumping = False

