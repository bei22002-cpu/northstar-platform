def create_game_project(
    name: str = "",
    framework: str = "pygame",
    genre: str = "platformer",
) -> str:
    """Create a new game project with the specified framework and genre.

    Generates all necessary files in agent_v2/game_projects/<name>/.
    """
    if not name:
        name = f"my_{genre}_game"

    # Sanitize name
    safe_name = "".join(c if c.isalnum() or c in "-_" else "_" for c in name)
    project_dir = os.path.join(_PROJECTS_DIR, safe_name)

    if os.path.isdir(project_dir):
        return f"Error: project '{safe_name}' already exists at {project_dir}"

    # Select generator
    files: dict[str, str] = {}

    if framework == "pygame":
        generator = _PYGAME_GENERATORS.get(genre)
        if generator:
            files = generator(name)
        else:
            available = ", ".join(sorted(_PYGAME_GENERATORS.keys()))
            return (
                f"Error: no Pygame template for genre '{genre}'. "
                f"Available: {available}"
            )
    elif framework == "phaser":
        generator = _PHASER_GENERATORS.get(genre)
        if generator:
            files = generator(name)
        else:
            return (
                f"Error: no Phaser template for genre '{genre}'. "
                f"Available: platformer"
            )
    else:
        available_fw = ", ".join(sorted(FRAMEWORKS.keys()))
        return (
            f"Error: no templates for framework '{framework}' yet. "
            f"Available with templates: pygame, phaser. "
            f"All frameworks: {available_fw}"
        )

    # Write files
    os.makedirs(project_dir, exist_ok=True)
    for filename, content in files.items():
        filepath = os.path.join(project_dir, filename)
        os.makedirs(os.path.dirname(filepath), exist_ok=True)
        with open(filepath, "w", encoding="utf-8") as fh:
            fh.write(content)

    # Register project
    _register_project(safe_name, framework, genre, project_dir)

    fw_info = FRAMEWORKS.get(framework, {})
    return (
        f"Game project created: {project_dir}\n\n"
        f"Framework: {fw_info.get('name', framework)}\n"
        f"Genre: {GENRES.get(genre, {}).get('name', genre)}\n"
        f"Files: {', '.join(files.keys())}\n\n"
        f"To run:\n"
        f"  1. cd {project_dir}\n"
        f"  2. {fw_info.get('install', '')}\n"
        f"  3. {fw_info.get('run', '')}\n"
    )


def clone_game_repo(repo_url: str, name: str = "") -> str:
    """Clone a game repository from GitHub into the game projects directory."""
    os.makedirs(_PROJECTS_DIR, exist_ok=True)

    if not name:
        # Extract repo name from URL
        name = repo_url.rstrip("/").split("/")[-1]
        if name.endswith(".git"):
            name = name[:-4]

    project_dir = os.path.join(_PROJECTS_DIR, name)
    if os.path.isdir(project_dir):
        return f"Error: directory '{name}' already exists at {project_dir}"

    try:
        result = subprocess.run(
            ["git", "clone", repo_url, project_dir],
            capture_output=True,
            text=True,
            timeout=120,
        )
        if result.returncode != 0:
            return f"Error cloning repo: {result.stderr}"

        _register_project(name, "custom", "custom", project_dir)
        return (
            f"Repository cloned: {project_dir}\n"
            f"Source: {repo_url}\n\n"
            f"Check the README for setup and run instructions."
        )
    except subprocess.TimeoutExpired:
        return "Error: git clone timed out after 120 seconds."
    except Exception as exc:
        return f"Error: {exc}"


